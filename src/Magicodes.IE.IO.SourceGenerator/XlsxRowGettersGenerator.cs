        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var candidates = context.SyntaxProvider.CreateSyntaxProvider(
                predicate: (node, _) => node is TypeDeclarationSyntax,
                transform: (ctx, _) =>
                {
                    if (ctx.Node is TypeDeclarationSyntax tds)
                    {
                        var sym = ctx.SemanticModel.GetDeclaredSymbol(tds);
                        if (sym is not null && HasAttr(sym) && sym is INamedTypeSymbol nts)
                            return new TypeInfo { Syntax = tds, Symbol = nts };
                    }
                    return null;
                })
                .Where(t => t is not null && t.Syntax is not null && t.Symbol is not null);

            var all = candidates.Collect();
            context.RegisterSourceOutput(all, (spc, items) => Emit(spc, items));
        }

        private static bool HasAttr(ISymbol sym)
        {
            const string expected = "global::Magicodes.IE.IO.XlsxExportableAttribute";
            foreach (var attr in sym.GetAttributes())
                if (attr.AttributeClass?.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat) == expected)
                    return true;
            return false;
        }

        private static void Emit(SourceProductionContext spc, ImmutableArray<TypeInfo?> items)
        {
            if (items.IsDefaultOrEmpty) return;
            var seen = new HashSet<string>();

            foreach (var item in items)
            {
                if (item?.Symbol is null) continue;
                var named = item.Symbol;
                if (!seen.Add(named.ToDisplayString())) continue;

                try
                {
                    var source = Generate(named);
                    if (source is not null)
                        spc.AddSource($"XlsxGetters_{GetHintName(named)}.g.cs", SourceText.From(source, Encoding.UTF8));
                }
                catch (Exception ex)
                {
                    spc.ReportDiagnostic(Diagnostic.Create(
                        new DiagnosticDescriptor(
                            "MIEIO001",
                            "XlsxRowGetters source generation failed",
                            "Source generation for type '{0}' failed: {1}",
                            "Magicodes.IE.IO.SourceGenerator",
                            DiagnosticSeverity.Warning,
                            isEnabledByDefault: true),
                        item.Syntax!.GetLocation(),
                        named.ToDisplayString(), ex.Message));
                }
            }
        }

        private static string? Generate(INamedTypeSymbol type)
