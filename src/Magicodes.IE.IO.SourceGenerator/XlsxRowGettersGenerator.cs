                sb.Append("                    (w, row, styleId) =>\r\n");
                sb.Append("                    {\r\n");
                sb.Append(DirectWrite(p, "row." + EscapeIdentifier(p.Name), "styleId"));
                sb.Append("                    },\r\n");
                sb.Append("                    ").Append(setter).Append("),\r\n");
            }
            sb.Append("            });\r\n");
        }

        private static AttributeData? GetAttribute(IPropertySymbol property, string name)
        {
            foreach (var attribute in property.GetAttributes())
            {
                if (attribute.AttributeClass?.Name == name || attribute.AttributeClass?.Name == name.Replace("Attribute", string.Empty))
                    return attribute;
            }
            return null;
        }

        private static string? GetNamedString(AttributeData? attribute, string name)
        {
            if (attribute is null) return null;
            foreach (var argument in attribute.NamedArguments)
                if (argument.Key == name && argument.Value.Value is string value)
                    return value;
            return null;
        }

        private static bool GetNamedBool(AttributeData? attribute, string name)
        {
            if (attribute is null) return false;
            foreach (var argument in attribute.NamedArguments)
                if (argument.Key == name && argument.Value.Value is bool value)
                    return value;
            return false;
        }

        private static int? GetNamedInt(AttributeData? attribute, string name)
        {
