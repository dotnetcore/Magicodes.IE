        private static ITypeSymbol Underlying(ITypeSymbol t)
        {
            if (t is INamedTypeSymbol n && n.ConstructedFrom?.ToString() == "System.Nullable<T>" && n.TypeArguments.Length == 1)
                return n.TypeArguments[0];
            return t;
        }

        private static bool IsNullableValueType(ITypeSymbol t) =>
            t is INamedTypeSymbol n
            && n.ConstructedFrom?.ToString() == "System.Nullable<T>"
            && n.TypeArguments.Length == 1;

        private static bool IsInt(ITypeSymbol t)
        {
            var s = t.SpecialType;
            return s == SpecialType.System_Int16 || s == SpecialType.System_Byte || s == SpecialType.System_SByte
                || s == SpecialType.System_UInt16 || s == SpecialType.System_UInt32 || s == SpecialType.System_UInt64;
        }

        private static bool IsUlongEnum(ITypeSymbol t)
        {
            return t.TypeKind == TypeKind.Enum
                && t is INamedTypeSymbol n
                && n.EnumUnderlyingType?.SpecialType == SpecialType.System_UInt64;
        }

        private static bool IsDateTimeOffset(ITypeSymbol t)
        {
            return t is INamedTypeSymbol n
                && n.SpecialType == SpecialType.None
                && n.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat) == "global::System.DateTimeOffset";
        }

        private static string EscapeIdentifier(string name)
        {
            return Microsoft.CodeAnalysis.CSharp.SyntaxFacts.GetKeywordKind(name) != Microsoft.CodeAnalysis.CSharp.SyntaxKind.None
                || Microsoft.CodeAnalysis.CSharp.SyntaxFacts.GetContextualKeywordKind(name) != Microsoft.CodeAnalysis.CSharp.SyntaxKind.None
                ? "@" + name
                : name;
        }
