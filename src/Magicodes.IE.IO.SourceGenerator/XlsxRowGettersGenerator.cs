            }
            sb.Append("                return td;\r\n");
            sb.Append("            });\r\n");
            EmitRowWriters(sb, props, fullName);
            EmitMetadata(sb, props, fullName);
            sb.Append("        }\r\n");
            sb.Append("    }\r\n");
            if (!string.IsNullOrEmpty(ns) && ns != "global") sb.Append("}\r\n");

            return sb.ToString();
        }

