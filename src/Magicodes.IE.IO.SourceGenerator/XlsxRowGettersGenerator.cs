                sb.Append("                    ").Append(GetNamedDouble(exporter, "Width") is double width && width > 0 ? width.ToString(System.Globalization.CultureInfo.InvariantCulture) : "null").Append(",\r\n");
                sb.Append("                    ").Append(GetNamedInt(exporter, "Index")?.ToString(System.Globalization.CultureInfo.InvariantCulture) ?? "-1").Append(",\r\n");
