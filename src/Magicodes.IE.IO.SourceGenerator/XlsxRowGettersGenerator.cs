                var setter = CanGenerateSetter(p)
                    ? "(item, cell, converters, date1904) =>\r\n                {\r\n"
                        + "                    if (!global::Magicodes.IE.IO.XlsxGeneratedReadHelper.TryParse<" + TypeName(propertyType) + ">(cell, out var value, converters, date1904)) return false;\r\n"
