                sb.Append("                new global::Magicodes.IE.IO.XlsxGeneratedPropertyMetadata<").Append(fullName).Append(">(\r\n");
                sb.Append("                    ").Append(Quote(p.Name)).Append(",\r\n");
                sb.Append("                    ").Append(Quote(GetNamedString(importer, "Name"))).Append(",\r\n");
                sb.Append("                    ").Append(Quote(GetNamedString(display, "Name"))).Append(",\r\n");
                sb.Append("                    ").Append(Quote(GetConstructorString(description))).Append(",\r\n");
