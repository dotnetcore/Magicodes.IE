                    return $"{access}.HasValue ? global::Magicodes.IE.IO.CellValue.FromInteger((long){access}.Value) : global::Magicodes.IE.IO.CellValue.Null";
                }
                if (u.SpecialType == SpecialType.System_UInt64) return $"{access}.HasValue ? global::Magicodes.IE.IO.CellValue.FromNumber((double){access}.Value) : global::Magicodes.IE.IO.CellValue.Null";
                if (IsInt(u)) return $"{access}.HasValue ? global::Magicodes.IE.IO.CellValue.FromInteger((long){access}.Value) : global::Magicodes.IE.IO.CellValue.Null";
