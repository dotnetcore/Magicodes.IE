            }
            sb.Append("    internal static class ").Append(name).Append("_XlsxGetters\r\n");
            sb.Append("    {\r\n");

            foreach (var p in props)
            {
                sb.Append("        public static global::Magicodes.IE.IO.CellValue Get_").Append(p.Name)
                  .Append("(").Append(fullName).Append(" item) => ")
                  .Append(Expr(p, "item")).Append(";\r\n");
            }
            sb.Append("\r\n");

            sb.Append("        [System.Runtime.CompilerServices.ModuleInitializer]\r\n");
            sb.Append("        internal static void Register()\r\n");
            sb.Append("        {\r\n");
            sb.Append("            global::Magicodes.IE.IO.XlsxGeneratedGettersRegistry.Register<").Append(fullName).Append(">(() =>\r\n");
            sb.Append("            {\r\n");
            sb.Append("                var d = new System.Collections.Generic.Dictionary<string, System.Func<object?, global::Magicodes.IE.IO.CellValue>>(").Append(props.Count).Append(");\r\n");
            foreach (var p in props)
            {
                sb.Append("                d[\"").Append(p.Name).Append("\"] = obj =>\r\n");
                sb.Append("                {\r\n");
                sb.Append("                    var item = (").Append(fullName).Append(")obj!;\r\n");
                sb.Append("                    return ").Append(Expr(p, "item")).Append(";\r\n");
                sb.Append("                };\r\n");
            }
            sb.Append("                return d;\r\n");
            sb.Append("            });\r\n");
            sb.Append("            global::Magicodes.IE.IO.XlsxGeneratedTypedGettersRegistry.Register<").Append(fullName).Append(">(() =>\r\n");
            sb.Append("            {\r\n");
            sb.Append("                var td = new System.Collections.Generic.Dictionary<string, System.Func<").Append(fullName).Append(", global::Magicodes.IE.IO.CellValue>>(").Append(props.Count).Append(");\r\n");
            foreach (var p in props)
            {
