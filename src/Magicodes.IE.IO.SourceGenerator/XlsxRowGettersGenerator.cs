                if (u.SpecialType == SpecialType.System_Int64) return $"{access}.HasValue ? global::Magicodes.IE.IO.CellValue.FromInteger({access}.Value) : global::Magicodes.IE.IO.CellValue.Null";
                if (u.SpecialType == SpecialType.System_Double) return $"{access}.HasValue ? global::Magicodes.IE.IO.CellValue.FromNumber({access}.Value) : global::Magicodes.IE.IO.CellValue.Null";
