        {
            sb.Append("            global::Magicodes.IE.IO.XlsxGeneratedTypeMetadataRegistry.Register<").Append(fullName).Append(">(() => new global::Magicodes.IE.IO.XlsxGeneratedPropertyMetadata<").Append(fullName).Append(">[]\r\n");
            sb.Append("            {\r\n");
            foreach (var p in props)
            {
                var exporter = GetAttribute(p, "ExporterHeaderAttribute");
                var importer = GetAttribute(p, "ImporterHeaderAttribute");
