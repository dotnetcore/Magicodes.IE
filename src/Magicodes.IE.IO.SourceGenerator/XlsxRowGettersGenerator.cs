        private static string? GetConstructorString(AttributeData? attribute)
        {
            if (attribute is null || attribute.ConstructorArguments.Length == 0)
                return null;
            return attribute.ConstructorArguments[0].Value as string;
        }

        private static bool CanGenerateSetter(IPropertySymbol property)
        {
            return property.SetMethod is not null
                && property.SetMethod.DeclaredAccessibility == Accessibility.Public
                && !property.SetMethod.IsInitOnly;
        }

        private static ITypeSymbol GetParseType(ITypeSymbol type)
        {
            if (type is INamedTypeSymbol named
                && named.OriginalDefinition.SpecialType == SpecialType.System_Nullable_T)
                return named.TypeArguments[0];
            return type;
        }

        private static string TypeName(ITypeSymbol type)
        {
            return type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        }

        private static string Quote(string? value)
        {
            if (value is null) return "null";
            var sb = new StringBuilder(value.Length + 2);
            sb.Append('"');
            foreach (var ch in value)
            {
                switch (ch)
                {
                    case '\\': sb.Append("\\\\"); break;
                    case '"': sb.Append("\\\""); break;
                    case '\r': sb.Append("\\r"); break;
                    case '\n': sb.Append("\\n"); break;
                    case '\t': sb.Append("\\t"); break;
                    case '\0': sb.Append("\\0"); break;
                    default:
                        if (char.IsControl(ch))
                            sb.Append("\\u").Append(((int)ch).ToString("x4", System.Globalization.CultureInfo.InvariantCulture));
                        else
                            sb.Append(ch);
                        break;
                }
            }
            sb.Append('"');
            return sb.ToString();
        }

        private static string GetHintName(INamedTypeSymbol type)
        {
            var full = type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
            var sb = new StringBuilder(full.Length);
            foreach (var ch in full)
                sb.Append(char.IsLetterOrDigit(ch) ? ch : '_');
            return sb.ToString();
        }

        private static string GetNamespace(INamedTypeSymbol type)
        {
            var ns = type.ContainingNamespace;
            if (ns is null || ns.IsGlobalNamespace) return string.Empty;
            var parts = new Stack<string>();
            while (ns is not null && !ns.IsGlobalNamespace)
            {
                parts.Push(ns.Name);
                ns = ns.ContainingNamespace;
            }
            return string.Join(".", parts);
        }

        private static string Expr(IPropertySymbol p, string v)
